def saludar_p(nombre, saludo="Hola"):
    """Esta función saluda con un saludo personalizado."""
    print(f"{saludo}, {nombre}!")

# Llamadas a la función
saludar_p("Ana")  # Utiliza el saludo por defecto "Hola"
saludar_p("Carlos", saludo="¡Saludos")  # Utiliza el saludo proporcionado
saludar_p(f"James!") #
saludar_p("Nikolas",saludo="Hola!!") # Utiliza el saludo
